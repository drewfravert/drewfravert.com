const Helpers = {

  bindKeys(keys, callback) {

    const handler = [b.event.keydown, (event) => {

      const key = event.key;
      const keyMatch = keys.includes(key);

      keyMatch && callback();

    }];

    window.addEventListener(...handler);

  }

};

/*
==========================================================================================
  Private Functions
==========================================================================================
*/

/*
==========================================================================================
  Exports
==========================================================================================
*/

export default Helpers;
